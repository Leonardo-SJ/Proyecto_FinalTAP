 private void LoginAdmin() {
        if (!txt_usuarioAdmin.getText().isEmpty() && !txt_passwordAdministrado.getText().isEmpty()) {
            Ctrl_Empleado controlUsuario = new Ctrl_Empleado();
            Empleado usuario = new Empleado();
            usuario.setUsuario(txt_usuarioAdmin.getText().trim());
            usuario.setPassword(txt_passwordAdministrado.getText().trim());
            if (controlUsuario.loginUser(usuario)) {
                //JOptionPane.showMessageDialog(null, "Login Correcto...");
                FrmAdministrador menu = new FrmAdministrador();
                menu.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Usuario o Clave Incorrectos");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese sus credenciales");
        }
    }
