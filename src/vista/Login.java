    private void btnIniciarSeccionAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSeccionAdminActionPerformed
              // Crear conexi√≥n
    Conexion conexion = new Conexion();

    // Obtener valores de los campos de entrada
    String nombre = txt_usuarioAdmin.getText().trim();
    String contrasena = new String(txt_passwordAdministrado.getText().trim());

    // Validar que no haya campos vac√≠os
    if (nombre.isEmpty() || contrasena.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nombre y contrase√±a son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Consulta para validar usuario
    final String QUERY_SQL = "SELECT tipo FROM tb_usuario WHERE usuario = ? AND password = ?";

    try (Connection conn = conexion.conectar();
         PreparedStatement stmt = conn.prepareStatement(QUERY_SQL)) {

        // Establecer los par√°metros de la consulta
        stmt.setString(1, nombre);
        stmt.setString(2, contrasena);  // Aseg√∫rate de usar un hash si la contrase√±a est√° encriptada en la BD

        // Ejecutar la consulta
        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            String tipo = rs.getString("tipo");

            if ("administrador".equalsIgnoreCase(tipo)) {
                // Permitir el acceso
                JOptionPane.showMessageDialog(this, "Bienvenido " + nombre + " (Administrador)");
                this.LoginAdmin();
                // Aqu√≠ podr√≠as redirigir al usuario a otra ventana, si es necesario
            } else if ("empleado".equalsIgnoreCase(tipo)) {
                // Mostrar mensaje de error para administradores
                JOptionPane.showMessageDialog(this, "Usuario o contrase√±a incorrectos.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Usuario no encontrado en la base de datos
            JOptionPane.showMessageDialog(this, "Usuario o contrase√±a incorrectos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al validar el usuario: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnIniciarSeccionAdminActionPerformed

